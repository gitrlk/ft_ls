#include "libft.h"

void		*ft_bzero(void *s, size_t n)
{
	char	*str;
	size_t	j;

	j = 0;
	str = (char *)s;
	while (j < n)
	{
		str[j] = 0;
		j++;
	}
	return ((void*)s);
}
